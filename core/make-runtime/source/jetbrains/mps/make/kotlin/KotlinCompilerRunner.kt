package jetbrains.mps.make.kotlin

import jetbrains.mps.make.CompositeTracer
import jetbrains.mps.util.PathManager
import org.jetbrains.kotlin.cli.common.CompilerSystemProperties
import org.jetbrains.kotlin.daemon.client.*
import org.jetbrains.kotlin.daemon.client.KotlinCompilerClient.connectAndLease
import org.jetbrains.kotlin.daemon.common.*
import org.jetbrains.kotlin.daemon.common.FileSystem.tempPath
import java.io.Closeable
import java.io.File
import java.io.IOException
import java.nio.file.Files
import java.nio.file.Path
import java.nio.file.Paths


/**
 * Utility object to run the kotlin compilation using daemon. This object will connect to
 * the daemon once and can run several compilations in a row.
 *
 * When usage is over, dispose() function should be called. This object is not designed to be kept for
 * a long time (as not disposing it keeps the daemon alive).
 */
abstract class KotlinCompilerRunner(
    protected val logger: CompositeTracer,
    kotlinCompilerOptions: KotlinCompilerOptions?
) : Closeable {
    /**
     * Flag that indicates MPS is still alive, whose location provided from [KotlinCompilerOptions].
     * Creation and deletion of this file is not handled in ModuleMaker/CompilerRunner.
     */
    private val clientAliveFlagFile: File = kotlinCompilerOptions?.clientFile ?: KotlinCompilerClient.getOrCreateClientFlagFile(DAEMON_OPTIONS)

    /**
     * Flag that indicates the make session is still alive, created and deleted by [KotlinCompilerRunner].
     * Once the compilation is over (all cycles done), this file should be deleted to indicate kotlin compiler
     * daemon the session is done.
     */
    private val sessionAliveFlagFile: File = makeAutodeletingFlagFile("compiler-jps-session-", File(DAEMON_OPTIONS.runFilesPathOrDefault))
    private var serviceSession: CompileServiceSession? = null

    abstract val compileOptions: CompilationOptions
    abstract fun getCompilerArgs(modulesToCompile: List<KotlinModule>): Array<String>

    /**
     * Returns the known output files generated by the Kotlin compiler for the given list of modules.
     *
     * @param modulesCompiled the list of modules that were compiled **successfully**
     * @return a map containing the output files with their associated sources, generated by the Kotlin compiler for each module
     */
    open fun knownOutputFiles(modulesCompiled: List<KotlinModule>): Map<KotlinModule, Map<File, List<File>>> = emptyMap()

    /**
     * Runs the compilation of provided modules issued from ModuleMaker.
     *
     * @param modulesToCompile modules to be compiled
     * @return results from the compilation
     */
    fun doCompile(
        modulesToCompile: List<KotlinModule>,
        moduleByInputFile: Map<File, KotlinModule>
    ): KotlinCompilationOutput {
        // Then compile
        try {
            val session = connectToCompiler()
            if (session == null) {
                logger.error("unable to connect to kotlin compile service")
                return KotlinCompilationOutput.ABORTED
            }

            // Create collector and facade with it
            val messageCollector = KotlinCompilationOutputCollector(logger, moduleByInputFile)
            val facade = BasicCompilerServicesWithResultsFacadeServer(
                messageCollector, messageCollector, SOCKET_ANY_FREE_PORT
            )

            // Provided compilation results do not seem to be of any use currently
            compile(session, modulesToCompile, facade)

            val outputFiles = messageCollector.outputFiles +
                    knownOutputFiles(modulesToCompile).takeIf { messageCollector.result.isOk }.orEmpty()

            // Return collected result
            return KotlinCompilationOutput(outputFiles, messageCollector.result)
        } catch (e: IOException) {
            logger.error(e.localizedMessage)
            return KotlinCompilationOutput.ABORTED
        }
    }

    protected open fun compile(
        session: CompileServiceSession,
        modulesToCompile: List<KotlinModule>,
        facade: BasicCompilerServicesWithResultsFacadeServer
    ) {
        session.compileService.compile(
            session.sessionId,
            compilerArguments = getCompilerArgs(modulesToCompile)
                    + "-Xreport-output-files=true"
                    + "-Xmulti-platform",
            compileOptions,
            servicesFacade = facade,
            compilationResults = null
        )
    }


    /**
     * Create a compilation session with kotlin compiler daemon
     *
     * @return session, or null
     */
    private fun connectToCompiler(): CompileServiceSession? {
        // Session is cached
        if (serviceSession != null) {
            return serviceSession
        }

        // Following implementation was taken from newDaemonConnection() from https://github.com/JetBrains/kotlin/blob/4aed9511f693934b3f0037341148614c7c45c64a/compiler/compiler-runner-unshaded/src/org/jetbrains/kotlin/compilerRunner/KotlinCompilerRunnerUtils.kt
        // which is not (yet?) provided by the client jar
        val daemonReportMessages = ArrayList<DaemonReportMessage>()
        val daemonReportingTargets = DaemonReportingTargets(null, daemonReportMessages, null, null)

        // Actually connect to compiler
        val connection = connectAndLease(
            createCompilerId(),
            clientAliveFlagFile,
            DAEMON_JVM_OPTIONS,
            DAEMON_OPTIONS,
            daemonReportingTargets,
            autostart = true,
            leaseSession = true,
            sessionAliveFlagFile = sessionAliveFlagFile
        )

        // Get messages in case of failure
        if (connection == null) {
            daemonReportMessages.forEach { logger.log(it) }
        }

        return connection.also { serviceSession = it }
    }

    override fun close() {
        sessionAliveFlagFile.delete()
        serviceSession = null
    }

    companion object {
        /**
         *
         * Options for the kotlin compiler daemon
         * <br></br>
         *
         * All default daemon option EXCEPT runFilesPath: it's default value (when calling `new DaemonOptions()`) make classloading fails
         * because it requires CapitalizeDecapitalizeKt (from OSKind usage of toLowerCaseAsciiOnly) which is nowhere to be found at runtime.
         */
        val DAEMON_OPTIONS = DaemonOptions(
            tempPath,
            COMPILE_DAEMON_MEMORY_THRESHOLD_INFINITE,
            COMPILE_DAEMON_DEFAULT_IDLE_TIMEOUT_S,
            COMPILE_DAEMON_DEFAULT_UNUSED_TIMEOUT_S,
            COMPILE_DAEMON_DEFAULT_SHUTDOWN_DELAY_MS,
            COMPILE_DAEMON_FORCE_SHUTDOWN_DEFAULT_TIMEOUT_MS,
            verbose = false, reportPerf = false
        ).also { opts ->
            // Allow verbose report
            CompilerSystemProperties.COMPILE_DAEMON_VERBOSE_REPORT_PROPERTY.value?.let { opts.verbose = true }
        }

        val DAEMON_JVM_OPTIONS = configureDaemonJVMOptions(
            inheritMemoryLimits = true,
            inheritOtherJvmOptions = false,
            inheritAdditionalProperties = true
        )

        private val toolsJar: File?
            get() {
                // TODO taken from CompilerRunnerUtil (jps-plugin), is there something similar in MPS? do we really need tools jar in MPS or java version is ensured to be > 9?
                val javaHomePath = System.getProperty("java.home").takeIf { !it.isNullOrEmpty() } ?: return null

                Paths.get(javaHomePath).let { javaHome ->
                    javaHome.resolve("lib/tools.jar").let { if (Files.exists(it)) return it.toFile() }

                    // We might be inside jre.
                    if ("jre" == javaHome.fileName?.toString()) {
                        return javaHome.resolveSibling("lib/tools.jar").takeIf { Files.exists(it) }?.toFile()
                    }
                }

                return null
            }

        /**
         * Returns a compiler ID for MPS, it contains classpath and version of current MPS compiler
         * @return id of the compiler
         */
        private fun createCompilerId(): CompilerId {
            val kotlinCompilerLib = Path.of(PathManager.getPreInstalledPluginsPath(), "mps-kotlin", "kotlinc", "lib").toFile()

            val toolsJar = toolsJar
            val compilerJar = File(kotlinCompilerLib, "kotlin-compiler.jar")
            val daemonJar = File(kotlinCompilerLib, "kotlin-daemon.jar")

            // If there's a tools.jar, it should probably be included (java version <= 9)
            return CompilerId.makeCompilerId(listOfNotNull(compilerJar, daemonJar, toolsJar))
        }
    }
}
